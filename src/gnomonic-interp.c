        cv[ 7] = * ( bm + bm_d * ( bm_w * py   + pxp2 ) + bm_c );
        cv[ 8] = * ( bm + bm_d * ( bm_w * pyp1 + pxm1 ) + bm_c );
        cv[ 9] = * ( bm + bm_d * ( bm_w * pyp1 + px   ) + bm_c );
        cv[10] = * ( bm + bm_d * ( bm_w * pyp1 + pxp1 ) + bm_c );
        cv[11] = * ( bm + bm_d * ( bm_w * pyp1 + pxp2 ) + bm_c );
